using System;
using System.Linq;
using System.Threading.Tasks;
using SSY.Products.OrderStatuses.Dtos;
using Volo.Abp.Application.Dtos;
using Volo.Abp.Application.Services;
using Volo.Abp.Domain.Repositories;
using Volo.Abp;

namespace SSY.Products.OrderStatuses;

public class OrderStatusAppService : CrudAppService<OrderStatus, OrderStatusDto, int, PagedAndSortedResultRequestDto, CreateOrderStatusDto, UpdateOrderStatusDto>, IOrderStatusAppService
{
    public OrderStatusAppService(IRepository<OrderStatus, int> repository) : base(repository)
    {
    }

    protected override async Task<IQueryable<OrderStatus>> CreateFilteredQueryAsync(PagedAndSortedResultRequestDto input)
    {
        try
        {
            return await base.CreateFilteredQueryAsync(input);
        }
        catch (Exception ex)
        {
            string innerException = string.Empty;
            if (ex.InnerException != null)
                innerException = $"\nInnerException:{ex.InnerException.Message}";

            throw new UserFriendlyException($"{ex.Message}{innerException}");
        }
    }

    public override Task<OrderStatusDto> CreateAsync(CreateOrderStatusDto input)
    {
        try
        {
            return base.CreateAsync(input);
        }
        catch (Exception ex)
        {
            string innerException = string.Empty;
            if (ex.InnerException != null)
                innerException = $"\nInnerException:{ex.InnerException.Message}";

            throw new UserFriendlyException($"{ex.Message}{innerException}");
        }
    }

    public override Task<OrderStatusDto> UpdateAsync(int id, UpdateOrderStatusDto input)
    {
        try
        {
            return base.UpdateAsync(id, input);
        }
        catch (Exception ex)
        {
            string innerException = string.Empty;
            if (ex.InnerException != null)
                innerException = $"\nInnerException:{ex.InnerException.Message}";

            throw new UserFriendlyException($"{ex.Message}{innerException}");
        }
    }
}